    public void setRecordstoTable(){
            try{
               Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection con = (java.sql.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/fees_mange_sys","root","Chirag@123");
            PreparedStatement pst = con.prepareStatement("select * from coursedb");
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                String courseid = rs.getString("id");
               
                String courseName=rs.getString("coursename");
                String cost = rs.getString("coursecost");
                
                Object[] obj = {courseid,courseName,cost};
               model = (DefaultTableModel)tbl_courses.getModel();
               model.addRow(obj);
            }
            }catch(Exception e){
                e.printStackTrace();
            }
        }

