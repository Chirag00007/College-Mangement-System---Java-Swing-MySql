    public void addCourse(int id,String cname,double cost) 
    {
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection con = (java.sql.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/fees_mange_sys","root","Chirag@123");
            PreparedStatement pst = con.prepareStatement("insert into coursedb values(?,?,?)");
        pst.setInt(1, id);
        pst.setString(2, cname);
        pst.setDouble(3, cost);
        int i = pst.executeUpdate();
        if(i>0){
            JOptionPane.showMessageDialog(this, "Course added successfully");
            clearData();
            setRecordstoTable();
        }
        else{
             JOptionPane.showMessageDialog(this, "Failed");
        }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
